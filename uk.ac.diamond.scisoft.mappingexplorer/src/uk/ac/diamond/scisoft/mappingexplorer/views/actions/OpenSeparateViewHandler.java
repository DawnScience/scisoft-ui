package uk.ac.diamond.scisoft.mappingexplorer.views.actions;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.core.commands.IHandlerListener;
import org.eclipse.ui.IViewPart;
import org.eclipse.ui.IWorkbenchPage;
import org.eclipse.ui.IWorkbenchPart;
import org.eclipse.ui.PartInitException;
import org.eclipse.ui.PlatformUI;
import org.eclipse.ui.handlers.HandlerUtil;
import org.slf4j.Logger;

import uk.ac.diamond.scisoft.mappingexplorer.views.twod.IMappingDataControllingView;

/**
 * @author rsr31645
 * 
 */
public abstract class OpenSeparateViewHandler extends AbstractHandler {

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.commands.IHandler#addHandlerListener(org.eclipse.core
	 * .commands.IHandlerListener)
	 */
	@Override
	public void addHandlerListener(IHandlerListener handlerListener) {
		getLogger().debug("addHandlerListener {}", handlerListener);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.commands.IHandler#dispose()
	 */
	@Override
	public void dispose() {
		getLogger().debug("Dispose called");
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.commands.IHandler#execute(org.eclipse.core.commands.
	 * ExecutionEvent)
	 */
	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		IWorkbenchPart activePart = HandlerUtil.getActivePart(event);
		if (activePart instanceof IViewPart) {
			IViewPart vp = (IViewPart) activePart;
			if (vp.getViewSite().getSecondaryId() != null) {
				return null;
			}
		}

		if (activePart instanceof IMappingDataControllingView) {
			IMappingDataControllingView mappingDataControllingView = (IMappingDataControllingView) activePart;
			try {
				PlatformUI
						.getWorkbench()
						.getActiveWorkbenchWindow()
						.getActivePage()
						.showView(
								getViewIdToOpen(),
								mappingDataControllingView
										.getOriginIdentifier(),
								IWorkbenchPage.VIEW_VISIBLE);
			} catch (PartInitException e) {
				getLogger().error("Error activating 2D separate view {}", e);
			}
		}
		return null;
	}

	protected abstract String getViewIdToOpen();

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.commands.IHandler#isEnabled()
	 */
	@Override
	public boolean isEnabled() {
		return true;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.commands.IHandler#isHandled()
	 */
	@Override
	public boolean isHandled() {
		return true;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.commands.IHandler#removeHandlerListener(org.eclipse.
	 * core.commands.IHandlerListener)
	 */
	@Override
	public void removeHandlerListener(IHandlerListener handlerListener) {
		getLogger().debug("removeHandelrListener {}", handlerListener);
	}

	protected abstract Logger getLogger();

}
